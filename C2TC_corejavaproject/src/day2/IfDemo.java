package day2;
//In Java, when you write an if (or other control structures like for, while, etc.),
//if there's only one statement to execute, you can omit the curly braces {}.
public class IfDemo {
	 public static void main(String[] args) {
	        int x = 5;
	        if (x != 5)
	            System.out.println("Value of x is not 5");
	        System.out.println("Value of x is 5");
	    }
}
